
namespace SharpexGL.Framework.Common.Debug
{
    public enum LogLevel
    {
        /// <summary>
        /// Indicates an information.
        /// </summary>
        Info = 0, 
        /// <summary>
        /// Indicates a warning.
        /// </summary>
        Warning = 1,
        /// <summary>
        /// Indicates an error.
        /// </summary>
        Error = 2,
        /// <summary>
        /// Indicates a critical error.
        /// </summary>
        Critical = 3,
    }
}
