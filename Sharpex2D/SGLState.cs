
namespace Sharpex2D
{
    public enum SGLState
    {
        /// <summary>
        /// SGL is not initialized.
        /// </summary>
        NotInitialized = 0,
        /// <summary>
        /// SGL is currently initializing.
        /// </summary>
        Initializing = 1,
        /// <summary>
        /// SGL is initialized.
        /// </summary>
        Initialized = 2,
        /// <summary>
        /// SGL is running.
        /// </summary>
        Running = 3
    }
}
